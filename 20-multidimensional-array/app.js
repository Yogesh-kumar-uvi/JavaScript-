let matrix = [
    [1, 2, 3], // First row
    [4, 5, 6], // Second row
    [7, 8, 9]  // Third row
];

// Accessing elements
console.log(matrix[1][1]); // Output: 2 (element at first row, second column)
console.log(matrix[2][2]); // Output: 9 (element at third row, third column)

// Modifying elements
matrix[2][2] = "mango"; // Changes the middle element
console.log(matrix);


